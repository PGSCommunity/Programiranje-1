#include <iostream>
using namespace std;

int main() {
    int a, b;
    cout << "Unesite brojeve a i b!\n";
    cin >> a >> b;

    cout << "Njihov zbir: " << a + b << '\n';
    cout << "Njihova razlika: " << a - b << '\n';
    cout << "Njihov proizvod: " << a * b << '\n';
    cout << "Dvostruka vrijednost prvog broja: " << a * a;
    return 0;
}
