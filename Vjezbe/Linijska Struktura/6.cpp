#include <iostream>
using namespace std;

int main() {
    int a, b;
    cout << "Unesite dva broja!\n";
    cin >> a >> b;

    cout << "Suma: " << a + b << '\n';
    cout << "Proizvod: " << a * b << '\n';
    cout << "Razlika: " << a - b;
    return 0;
}
